# Block code
for i in range(10):
    print(i)

# case-sensitive: capital and lower case is different
teks = "Dicoding"
Teks = "Indonesia"

print(teks)
print(Teks)

# one liner: membuat satu baris kode yang singkat dan jelas
x = 1
y = 2

temp = x
x = y
y = temp

print("Setelah pertukaran: ")
print("x = ", x)
print("y = ", y)

# Code oneliner
x, y = y, x
print("Setelah pertukaran: ")
print("x = ", x)
print("y = ", y)
